  // ── Upcoming Events ───────────────────────────────
  static List<EventModel> get upcomingEvents =>
      events.where((e) => e.startDate.isAfter(DateTime.now())).toList()
        ..sort((a, b) => a.startDate.compareTo(b.startDate));

  // ── Nearby Events (offline) ───────────────────────
  static List<EventModel> get nearbyEvents =>
      events.where((e) => e.mode != EventMode.online).take(6).toList();

