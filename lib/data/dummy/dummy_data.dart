  // ── Get Recommended by Interests ─────────────────
  static List<EventModel> getRecommended(List<String> interests) {
    if (interests.isEmpty) return events.take(5).toList();
    final tags = interests.expand((i) => [i.toLowerCase()]).toList();
    return events.where((e) {
      return e.tags.any((t) => tags.any(
            (interest) => t.toLowerCase().contains(interest) ||
                interest.contains(t.toLowerCase()),
          ));
    }).toList();
  }

